// Capacity type icons mapping
export const capacityIcons: Record<string, LucideIcon> = {
  // New international icon names (from DB icon column)
  heart: Heart,
  search: Search,
  home: Home,
  droplets: Droplets,
  apple: Apple,
  truck: Truck,
  showerhead: ShowerHead,
  radio: Radio,
  zap: Zap,
  shovel: Shovel,
  brain: Brain,
  baby: Baby,
  graduationcap: GraduationCap,
  tent: Tent,
  sprout: Sprout,
  shield: Shield,
  users: Users,
  // Legacy icon names (for backwards compatibility)
  droplet: Droplet,
  utensils: Utensils,
  "heart-pulse": HeartPulse,
  "life-buoy": LifeBuoy,
  flame: Activity,
};

export function getCapacityIcon(iconName: string | null): LucideIcon {
  if (!iconName) return Activity;
  return capacityIcons[iconName.toLowerCase()] || Activity;
}

